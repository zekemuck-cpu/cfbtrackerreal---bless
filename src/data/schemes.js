// Default ST attrs when no scheme applies (K/P always use these for Default preset)
const ST_DEFAULTS = {
  K: ['Kick Power', 'Kick Accuracy', 'Awareness', 'Speed', 'Agility'],
  P: ['Kick Power', 'Kick Accuracy', 'Awareness', 'Speed', 'Agility'],
}

const OFFENSE_POSITIONS = new Set(['QB', 'HB', 'FB', 'WR', 'TE', 'LT', 'LG', 'C', 'RG', 'RT'])
const DEFENSE_POSITIONS = new Set(['LEDG', 'REDG', 'DT', 'SAM', 'MIKE', 'WILL', 'CB', 'FS', 'SS'])

// Returns the 5 key attributes for a given scheme + position.
// Returns null if the relevant scheme is not set.
export function getSchemeAttrs(position, offenseScheme, defenseScheme) {
  if (ST_DEFAULTS[position]) return ST_DEFAULTS[position]
  if (OFFENSE_POSITIONS.has(position)) {
    if (!offenseScheme) return null
    return SCHEME_ATTRS[offenseScheme]?.[position] || null
  }
  if (DEFENSE_POSITIONS.has(position)) {
    if (!defenseScheme) return null
    return SCHEME_ATTRS[defenseScheme]?.[position] || null
  }
  return null
}

// Returns the 5 key attributes for a given preset + position.
// For 'Default', delegates to the active scheme. Returns null when scheme is
// required but not set.
export function getPresetAttrs(preset, position, offenseScheme, defenseScheme) {
  if (preset === 'Default' || !preset) {
    return getSchemeAttrs(position, offenseScheme, defenseScheme)
  }
  return POSITION_PRESETS[preset]?.[position] || null
}

export function sideForPosition(position) {
  if (OFFENSE_POSITIONS.has(position)) return 'offense'
  if (DEFENSE_POSITIONS.has(position)) return 'defense'
  return 'st'
}
